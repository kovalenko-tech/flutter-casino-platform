/// Icon size tokens for the "Velvet & Gold" design system.
///
/// Apply consistently across all Icon widgets.
/// Never pass a raw double to Icon — use these constants.
abstract final class AppIconSize {
  /// 12 px — micro indicators, inline badges.
  static const double xs = 12;

  /// 16 px — dense list rows, small chips.
  static const double sm = 16;

  /// 20 px — bottom nav icons, secondary actions.
  static const double md = 20;

  /// 24 px — standard icon size, app bar actions.
  static const double lg = 24;

  /// 32 px — prominent single-icon buttons, feature highlights.
  static const double xl = 32;

  /// 48 px — empty-state illustrations, large standalone icons.
  static const double xxl = 48;
}
